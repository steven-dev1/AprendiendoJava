public class User {
    private String usuName;
    private String usuLastname;
    private String usuEmail;
    private String usuDate;
    private String usuUid;
    private String usuTel;
    private String usuPass;
    private int usuId;
    private int usuRol;

    public String getUsuName() {
        return usuName;
    }

    public void setUsuName(String usuName) {
        this.usuName = usuName;
    }

    public String getUsuLastname() {
        return usuLastname;
    }

    public void setUsuLastname(String usuLastname) {
        this.usuLastname = usuLastname;
    }

    public String getUsuEmail() {
        return usuEmail;
    }

    public void setUsuEmail(String usuEmail) {
        this.usuEmail = usuEmail;
    }

    public String getUsuDate() {
        return usuDate;
    }

    public void setUsuDate(String usuDate) {
        this.usuDate = usuDate;
    }

    public String getUsuUid() {
        return usuUid;
    }

    public void setUsuUid(String usuUid) {
        this.usuUid = usuUid;
    }

    public String getUsuTel() {
        return usuTel;
    }

    public void setUsuTel(String usuTel) {
        this.usuTel = usuTel;
    }

    public String getUsuPass() {
        return usuPass;
    }

    public void setUsuPass(String usuPass) {
        this.usuPass = usuPass;
    }

    public int getUsuId() {
        return usuId;
    }

    public void setUsuId(int usuId) {
        this.usuId = usuId;
    }

    public int getUsuRol() {
        return usuRol;
    }

    public void setUsuRol(int usuRol) {
        this.usuRol = usuRol;
    }
    
    
    
}
